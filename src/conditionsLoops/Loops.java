package conditionsLoops;

public class Loops {

	public static void main(String[] args) {
		/*Syntax of for loops:
  		for(initialization; condition; increment/decrement){
    		//body
		}
  		*/
		
		//Print numbers from 1 to 5
		for(int num = 1; num <=5; num++)
		{
			System.out.println(num);
		}
	}
}
